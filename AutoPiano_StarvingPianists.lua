local NOTE_MAP = {
    -- Октава 3
    C3="Z",  Cs3="S", D3="X",  Ds3="D", E3="C",  F3="V",
    Fs3="G", G3="B",  Gs3="H", A3="N",  As3="J", B3="M",
    -- Октава 4
    C4="Q",  Cs4="Two",   D4="W",  Ds4="Three", E4="E",  F4="R",
    Fs4="Five", G4="T",  Gs4="Six", A4="Y",  As4="Seven", B4="U",
    -- Октава 5
    C5="I",  Cs5="Nine",  D5="O",  Ds5="Zero",  E5="P",  F5="LeftBracket",
    Fs5="Equals", G5="RightBracket", Gs5="BackSlash", A5="BackSpace",
    As5="Insert", B5="Delete",
    -- Октава 6
    C6="Home",
}

local function playNote(n)
    n = n:gsub("%s",""):gsub("#","s"):gsub("b","s")
    local key = NOTE_MAP[n]
    if not key then return end
    local ok, vk = pcall(function() return game:GetService("VirtualInputManager") end)
    if not ok then return end
    local kc = Enum.KeyCode[key]
    if kc then
        vk:SendKeyEvent(true,  kc, false, game)
        task.wait(0.03)
        vk:SendKeyEvent(false, kc, false, game)
    end
end

-- ══════════════════════════════════════════
-- ХЕЛПЕР ДЛЯ СТРОКИ НОТ
-- n(note, delay) — задержка ПЕРЕД нотой в секундах
-- ══════════════════════════════════════════
local function N(...)
    local t={...}
    local r={}
    for i=1,#t,2 do r[#r+1]={t[i],t[i+1]} end
    return r
end

