local State = {playing=false, speed=1.0, thread=nil, collapsed=false}
local btns = {}

local function setStatus(t, c)
    SL.Text = t
    SL.TextColor3 = c or Color3.fromRGB(130,255,130)
end

local function stop()
    State.playing = false
    if State.thread then pcall(task.cancel, State.thread) State.thread=nil end
    setStatus("⏸ Остановлено", Color3.fromRGB(190,190,190))
end

local function play(name)
    stop()
    local s = SONGS[name] if not s then return end
    State.playing = true
    setStatus("▶ " .. name, Color3.fromRGB(130,255,130))
    State.thread = task.spawn(function()
        for i, nd in ipairs(s.notes) do
            if not State.playing then break end
            if i > 1 and nd[2] > 0 then
                task.wait(nd[2] / State.speed)
            end
            if not State.playing then break end
            playNote(nd[1])
        end
        if State.playing then
            State.playing = false
            setStatus("✅ " .. name, Color3.fromRGB(170,255,170))
        end
    end)
end

local function buildList(filter)
    for _,b in ipairs(btns) do b:Destroy() end btns = {}
    local cats, co = {}, {}
    for name, data in pairs(SONGS) do
        local show = filter=="" or name:lower():find(filter:lower(),1,true)
        if show then
            local c = data.cat or "Другое"
            if not cats[c] then cats[c]={} co[#co+1]=c end
            cats[c][#cats[c]+1] = name
        end
    end
    table.sort(co)
    for _,c in ipairs(co) do table.sort(cats[c]) end

    for _,cat in ipairs(co) do
        local cl = Instance.new("TextLabel")
        cl.Text = " " .. cat
        cl.Size = UDim2.new(1,0,0,19)
        cl.BackgroundColor3 = Color3.fromRGB(26,20,50)
        cl.TextColor3 = Color3.fromRGB(148,118,255)
        cl.TextSize = 10
        cl.Font = Enum.Font.GothamBold
        cl.BorderSizePixel = 0
        cl.TextXAlignment = Enum.TextXAlignment.Left
        cl.Parent = SF
        Instance.new("UICorner",cl).CornerRadius = UDim.new(0,5)
        btns[#btns+1] = cl

        for _,name in ipairs(cats[cat]) do
            local short = name:match("^[^%-]+%- (.+)") or name
            local btn = Instance.new("TextButton")
            btn.Text = "  " .. short
            btn.Size = UDim2.new(1,0,0,25)
            btn.BackgroundColor3 = Color3.fromRGB(16,14,28)
            btn.TextColor3 = Color3.fromRGB(196,188,228)
            btn.TextSize = 11
            btn.Font = Enum.Font.Gotham
            btn.BorderSizePixel = 0
            btn.TextXAlignment = Enum.TextXAlignment.Left
            btn.Parent = SF
            Instance.new("UICorner",btn).CornerRadius = UDim.new(0,5)
            local n = name
            btn.MouseButton1Click:Connect(function()
                for _,b in ipairs(btns) do
                    if b:IsA("TextButton") then
                        b.BackgroundColor3=Color3.fromRGB(16,14,28)
                        b.TextColor3=Color3.fromRGB(196,188,228)
                    end
                end
                btn.BackgroundColor3 = Color3.fromRGB(52,38,100)
                btn.TextColor3 = Color3.fromRGB(255,245,255)
                play(n)
            end)
            btns[#btns+1] = btn
        end
    end
end

